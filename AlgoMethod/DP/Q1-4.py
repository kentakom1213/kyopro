N = int(input())

DP = [1, 1, 2] + [0] * (N - 1)  # なんで DP[0] も 1 なんだろうか

if N > 1:
    for i in range(3, N+1):
        DP[i] = DP[i-1] + DP[i-2] + DP[i-3]

print(DP[N])