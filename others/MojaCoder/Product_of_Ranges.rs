// solve
fn main() {
    let (N, Q) = get!(usize, usize);
    let A = get!(usize;;);
    let queries = get!(usize, usize; Q);

    let P = {
        let mut p = vec![1_usize; N+1];
        for i in 0..N {
            p[i+1] = p[i].mmul(A[i]);
        }
        p
    };

    for &(a, b) in &queries {
        let res = P[b].mdiv(P[a-1]);
        println!("{}", res);
    }
}
