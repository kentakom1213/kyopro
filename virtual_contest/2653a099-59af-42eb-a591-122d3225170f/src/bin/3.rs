macro_rules! debug {
    ( $($val:expr),* $(,)* ) => {{
        #[cfg(debug_assertions)]
        eprintln!( concat!($(stringify!($val), " = {:?}, "),*), $($val),* );
    }};
}

/// `chmin!{x1, x2, ..., xn}`:`x1`,`x2`,...,`xn`のうち最小のものを、`x1`に代入する
/// - 代入があったとき、`true`を返す
#[macro_export]
macro_rules! chmin {
    ( $a:expr, $b:expr $(,)* ) => {{
        if $a > $b {
            $a = $b;
            true
        } else {
            false
        }
    }};
    ( $a:expr, $b:expr, $c:expr $(,$other:expr)* $(,)* ) => {{
        chmin! {
            $a,
            ($b).min($c)
            $(,$other)*
        }
    }};
}

// constant
const MOD1: usize = 1_000_000_007;
const MOD9: usize = 998_244_353;
const INF: usize = 1001001001001001001;

fn main() {
    input! {
        n: usize,
        x: usize,
        ab: [(usize, usize); n]
    }

    // すべてのパターンをシミュレートする
    let mut ans = INF;
    let mut sum = 0;

    for (i, &(a, b)) in ab.iter().enumerate() {
        sum += a + b;
        // 残り回数は、bを繰り返すことで埋める
        chmin! {
            ans,
            sum + (x - i - 1) * b
        };
    }

    println!("{ans}");
}
