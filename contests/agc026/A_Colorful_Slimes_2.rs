// solve
fn main() {
    let N = get!(usize);
    let A = get!(usize;;);

    let comp = run_length_encode(&A);

    let ans = comp
        .iter()
        .map(|&(c, _)| c)
        .map(|c| c / 2)
        .fold(0, |acc, v| acc + v);
    
    println!("{}", ans);
}
