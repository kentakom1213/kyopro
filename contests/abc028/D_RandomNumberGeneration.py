# 3つの数を小さい順に a, b, c とするとき
# a <= b <= c となる組合せを求めれば良い
# N^3で割ればいい

N, K = map(int, input().split())

comb = sum([
    (K - 1) * (N - K) * 6,  # 全て異なる
    (K - 1) * 3,            # b == c
    (N - K) * 3,            # a == b
    1                       # a == b == c
])

print(comb / N**3)
